const slider = document.getElementById('slider');
const resena = document.getElementById('resena');

libros.forEach((libro) => {
  const slide = document.createElement('div');
  slide.className = 'swiper-slide';
  slide.innerHTML = `
    <div class="recomanador">
      <img class="foto" src="${libro.fotoRecomendador}" alt="Foto de ${libro.recomendador}" />
      <div class="nombre">${libro.recomendador}</div>
    </div>
    <div class="portadaL">
      <img class="caratula" src="${libro.caratula}" alt="Carátula de ${libro.titulo}" />
    </div>
    <div class="info">
      <div class="titulo">${libro.titulo}</div>
      <div class="autor">${libro.autor}</div>
      <div class="editorial">${libro.editorial}</div>
    </div>
  `;
  slide.addEventListener('click', () => {
    resena.textContent = libro.resenya;
  });
  slider.appendChild(slide);
});

// resenya inicial
resena.textContent = libros[0].resenya;

